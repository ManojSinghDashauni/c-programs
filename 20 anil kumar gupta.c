/*
PROBLEM STATEMENT
WAP to convert a name to a given format as if ANIL KUMAR GUPTA to A.K.Gupta
*/

#include<stdio.h>

void main(){
    char Str1[10]="ANIL";
    char Str2[10]="KUMAR";
    char Str3[10]="GUPTA";

    printf("%.1s.%.1s.%s\n",Str1,Str2,Str3);
}

/*
OUTPUT
A.K.GUPTA
*/  	

